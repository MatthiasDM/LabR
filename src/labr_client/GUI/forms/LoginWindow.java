    public void eHealthLab() throws IOException {
        String nihiiNumber = PublicVars.getUserData()[5];
        String keystoreLocation = "\\properties\\ehealth\\be.ehealth.technicalconnector_lab.properties";
        Properties p = new Properties();
        ClassLoader classLoader = ClassLoader.getSystemClassLoader();
        InputStream is = classLoader.getResourceAsStream(keystoreLocation);
        
        p.load(new InputStreamReader(is,"UTF-8"));
        p.setProperty("KEYSTORE_DIR", PublicVars.getUserData()[8] + "\\"); //hier de locatie laden vanuit de database
        p.setProperty("user.nihii", nihiiNumber);
        p.setProperty("sessionmanager.samlattribute.1", "urn:be:fgov:identification-namespace,urn:be:fgov:ehealth:1.0:certificateholder:labo:nihii-number," + nihiiNumber);
        p.setProperty("sessionmanager.samlattribute.2", "urn:be:fgov:identification-namespace,urn:be:fgov:ehealth:1.0:labo:nihii-number," + nihiiNumber);
        p.store(new OutputStreamWriter(new FileOutputStream("data/tmp/eHealthProps.properties"),"UTF-8"), "");
        PublicVars.seteHealthProperties(p);
        PublicVars.seteHealthPropertiesLocation(PublicVars.getProperties().getProperty("ehealth_properties_lab"));
    }

